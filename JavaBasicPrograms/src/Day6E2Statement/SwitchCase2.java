package Day6E2Statement;

public class SwitchCase2 {

	public static void main(String[] args) {
         int month=1;
	switch (month) {
	case 1:
	       System.out.println("Month in January");
	            break;
	case 2:
	      System.out.println("Month in February");
	            break;
	case 3:
	      System.out.println("Month in March");
	            break;
	case 4:  
	      System.out.println("Month in April");
	            break;
	default:
	      System.out.println("Sorry non of your cases matched");
	}

}
}