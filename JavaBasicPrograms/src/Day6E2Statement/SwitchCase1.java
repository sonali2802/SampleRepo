package Day6E2Statement;

public class SwitchCase1 {

	public static void main(String[] args) {
         int value =8;
	   switch (value) {
	   case 3:
	System.out.println("Congratulation here is the case 3 executed");
	    break;
	   case 4:
		System.out.println("Congratulation here is the case 4 executed");
          break;
	   case 8:
		System.out.println("Congratulation here is the case 8 executed");
           break;
   		default:
           System.out.println("Sorry non of your cases matched");
            break;
	   
	   
	   }
	   
	   
	   }

}
