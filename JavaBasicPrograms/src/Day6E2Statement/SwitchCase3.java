package Day6E2Statement;

public class SwitchCase3 {

	public static void main(String[] args) {
		char ch ='o';
		switch (ch) {
		case 'e':
		       System.out.println("value matched-e,a vowel");
		            break;
		case 'a':
		      System.out.println("value matched -a,a vowle");
		            break;
		case 'u':
		      System.out.println("value matched -u,a vowle");
		            break;
		case 'o':  
		      System.out.println("value matched -o,a vowle");
		            break;
		case 'i':  
		      System.out.println("value matched -i,a vowle");
		            break;
		default:
		      System.out.println("given char is not vowle");
		             break;
		}

	
	
	
	
	
	}

}
