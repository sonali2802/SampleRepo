package Day6EAssignment;

public class DowhileLoop1 {

	public static void main(String[] args) {
      int c=10;
	System.out.println("Printing all the numbers from 10 till 1");
	  do {
	System.out.println(c);
	--c;
	  } while (c>0);
	
	}

}
//do-while execution order:
    //1.body
   // 2.condition

